ALTER SESSION SET NLS_NUMERIC_CHARACTERS = ',.';
SET LINESIZE 200
COLUMN TABLESPACE_NAME FORMAT A20
COLUMN FILE_NAME FORMAT A70

SELECT DT.TABLESPACE_NAME, DT.FILE_NAME, 
TO_CHAR(DT.MAXBYTES/1024/1024,'999G999G999D99') MXBYTES_MB,
TO_CHAR(DT.BYTES/1024/1024,'999G999G999D99') BYTES_MB
FROM DBA_TEMP_FILES DT
ORDER BY DT.TABLESPACE_NAME, DT.FILE_NAME;


